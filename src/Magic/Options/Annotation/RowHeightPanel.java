    public void initTable() {

        DefaultTableModel dm = (DefaultTableModel) row_height_table.getModel();
        while (dm.getRowCount() > 0) {
            dm.removeRow(0);
        }
        
        for (int i = 0; i < trackFrame.dataRep.trackNum; i++) {
            Vector<Object> vector = new Vector<Object>();
            String s1 = "" + (i + 1);
            String s2 = trackFrame.dataRep.tracks[i].trackSet.name;
            vector.add(s1);
            vector.add(s2);

            SpinnerModel model = new SpinnerNumberModel();
            JSpinner spinner = new JSpinner(model);
            
            spinner.setValue(trackFrame.dataRep.tracks[i].trackSet.trackHeight);
            model.addChangeListener(new javax.swing.event.ChangeListener() {
                public void stateChanged(javax.swing.event.ChangeEvent evt) {
                    SpinnerModel model=(SpinnerModel)(evt.getSource());
                    int index=-1;
                    for(int i=0;i<row_height_table.getRowCount();i++)
                    {
                        if(model.equals(((JSpinner)row_height_table.getValueAt(i,2)).getModel()))
                        {
                            index=i;
                            break;
                        }
                    }
                    //System.out.println(index+" "+model.getValue());
                    if(index>=0) trackFrame.receiveRowHeight(index,(Integer)model.getValue());
                }
            });
            vector.add(spinner);
            dm.addRow(vector);
        }
        
        row_height_table.getColumn(row_height_table.getColumnName(2)).setCellRenderer(new JSpinnerRenderer());
        row_height_table.getColumn(row_height_table.getColumnName(2)).setCellEditor(new JSpinnerEditor(new JCheckBox(), new JSpinner()));

    }

    public void update(MagicFrame parent)
    {
        trackFrame=parent;
        initTable();
    }

